import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { product } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are an expert skincare and beauty product analyst. When given product details, provide a comprehensive yet concise analysis covering:

1. **Ingredient Breakdown** – Highlight key active ingredients, what they do, and any potential concerns (allergens, irritants).
2. **Benefits Analysis** – Explain how the listed benefits work and rate their effectiveness.
3. **Skin Type Suitability** – Analyze which skin types (oily, dry, combination, sensitive, normal) this product is best/worst for.
4. **Usage Tips** – Best time to apply, how to layer with other products, frequency.
5. **Overall Verdict** – A brief 1-2 sentence summary rating.

Use emojis sparingly for visual appeal. Keep the tone friendly and informative. Format with markdown headers and bullet points.`;

    const userPrompt = `Analyze this beauty product:

**Name:** ${product.name}
**Description:** ${product.description}
**Ingredients:** ${product.ingredients}
**Benefits:** ${product.benefits?.join(", ") || "Not specified"}
**Suitable For:** ${product.suitable_for}
**Size:** ${product.size}
**Is Trial Size:** ${product.is_trial ? "Yes" : "No"}`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: userPrompt },
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI analysis unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("analyze-product error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
